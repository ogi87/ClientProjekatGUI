    private void ucitajKvalifikacije() {
        try {
            List<GenericEntity> lista = ClientController.getInstance().getAllKvalifikacija();

            List<Kvalifikacija> kvalifikacije = new ArrayList<>();

            for (GenericEntity ge : lista) {
                kvalifikacije.add((Kvalifikacija) ge);
            }

            ktm.setListaKvalifikacija(kvalifikacije);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greska pri ucitavanju kvalifikacija");
        }
    }

    private void dodajKvalifikaciju() {
        try {
            Kvalifikacija k = new Kvalifikacija();
            k.setNaziv(txtNaziv.getText());

            ClientController.getInstance().addKvalifikacija(k);

            JOptionPane.showMessageDialog(this, "Kvalifikacija dodata");
            ucitajKvalifikacije();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
