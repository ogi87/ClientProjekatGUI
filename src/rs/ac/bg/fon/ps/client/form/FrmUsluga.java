    private void dodajStavku() {
        try {
            Materijal materijal = (Materijal) cmbMaterijal.getSelectedItem();

            if (materijal == null) {
                JOptionPane.showMessageDialog(this, "Izaberite materijal.");
                return;
            }

            int kolicina = Integer.parseInt(txtKolicina.getText().trim());

            if (kolicina <= 0) {
                JOptionPane.showMessageDialog(this, "Kolicina mora biti veca od 0.");
                return;
            }

            StavkaUsluge stavka = new StavkaUsluge();
            stavka.setMaterijal(materijal);
            stavka.setKolicina(kolicina);
            stavka.setCena(materijal.getCena());
            stavka.setIznos(kolicina * materijal.getCena());

            sutm.addStavka(stavka);
            txtKolicina.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kolicina mora biti broj.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void obrisiStavku() {
        int row = tblStavke.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Izaberite stavku.");
            return;
        }

        sutm.removeStavka(row);
    }

    private void izracunaj() {
        try {
            double ukupan = 0;

            for (StavkaUsluge s : sutm.getLista()) {
                ukupan += s.getIznos();
            }

            double popust = 0;
            if (!txtPopust.getText().trim().isEmpty()) {
                popust = Double.parseDouble(txtPopust.getText().trim());
            }

            double saPopustom = ukupan - (ukupan * popust / 100.0);

            txtUkupanIznos.setText(String.valueOf(ukupan));
            txtUkupanSaPopustom.setText(String.valueOf(saPopustom));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Popust mora biti broj.");
        }
    }

    private void sacuvajIliIzmeniUslugu() {
        try {
            String naziv = txtNaziv.getText().trim();
            Zubar zubar = (Zubar) cmbZubar.getSelectedItem();
            Klijent klijent = (Klijent) cmbKlijent.getSelectedItem();

            if (naziv.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Unesite naziv usluge.");
                return;
            }

            if (zubar == null) {
                JOptionPane.showMessageDialog(this, "Izaberite zubara.");
                return;
            }

            if (klijent == null) {
                JOptionPane.showMessageDialog(this, "Izaberite klijenta.");
                return;
            }

            if (sutm.getLista().isEmpty()) {
                // KORAK 13.1: Hvatamo grešku već na klijentu
                JOptionPane.showMessageDialog(this, "Систем не може да запамти податке о услузи.", "Грешка", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (txtUkupanIznos.getText().trim().isEmpty() || txtUkupanSaPopustom.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Prvo kliknite na Izracunaj.");
                return;
            }

            Usluga u;
            boolean isKreiraj = false;

            if (kreiranaUsluga != null) {
                u = kreiranaUsluga; // Koristimo onu koja vec ima kreiran ID iz baze
                isKreiraj = true;
            } else if (uslugaZaIzmenu != null) {
                u = uslugaZaIzmenu; // Koristimo onu koju menjamo
            } else {
                JOptionPane.showMessageDialog(this, "Greska: Nije prosledjena usluga formi!");
                return;
            }

            u.setNaziv(naziv);
            u.setZubar(zubar);
            u.setKlijent(klijent);
            u.setStavke(new ArrayList<>(sutm.getLista()));
            u.setUkupanIznos(Double.parseDouble(txtUkupanIznos.getText().trim()));

            double popust = 0;
            if (!txtPopust.getText().trim().isEmpty()) {
                popust = Double.parseDouble(txtPopust.getText().trim());
            }

            u.setPopust(popust);
            u.setUkupanIznosSaPopustom(Double.parseDouble(txtUkupanSaPopustom.getText().trim()));

            if (isKreiraj) {
                ClientController.getInstance().zapamtiUslugu(u);
                JOptionPane.showMessageDialog(this, "Систем је запамтио услугу.", "Успех", JOptionPane.INFORMATION_MESSAGE);
            } else {

                ClientController.getInstance().zapamtiUslugu(u);
                JOptionPane.showMessageDialog(this, "Систем је запамтио податке о услузи.", "Успех", JOptionPane.INFORMATION_MESSAGE);

                if (parentForma != null) {

                    parentForma.osveziTabelu();
                }
            }

            dispose();

        } catch (Exception e) {
            // e.printStackTrace();
            if (kreiranaUsluga != null) {
                JOptionPane.showMessageDialog(this, "Систем не може да запамти услугу.", "Грешка", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Систем не може да запамти податке о услузи.", "Грешка", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void popuniFormu(Usluga u) {
        try {
            txtNaziv.setText(u.getNaziv());
            txtPopust.setText(String.valueOf(u.getPopust()));
            txtUkupanIznos.setText(String.valueOf(u.getUkupanIznos()));
            txtUkupanSaPopustom.setText(String.valueOf(u.getUkupanIznosSaPopustom()));

            cmbZubar.setSelectedItem(u.getZubar());
            cmbKlijent.setSelectedItem(u.getKlijent());

            if (u.getStavke() != null) {
                sutm.setLista(new ArrayList<>(u.getStavke()));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greska pri popunjavanju forme: " + e.getMessage());
        }
    }
